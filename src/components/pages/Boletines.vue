              </p>
              <ol>
                <li>El más importante: La relación directa de la empresa con el gobierno, dado que considera cualquier contacto con un servidor público como riesgoso para la organización, obligando a tener una atención clara del desenvolvimiento de dicha relación.</li>
                <li>Un procedimiento de Debida Diligencia (due diligence) que genere un mapa de riegos para investigar a los socios de negocios, verificando la conducta mostrada a través del tiempo en el ramo comercial donde se desempeñan. También se investiga la reputación de todas las personas que tienen una relación comercial con la empresa.</li>
