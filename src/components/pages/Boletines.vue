                <li>Control estricto de las áreas de compras, obligando a que los responsables directos de efectuarlas acepten contratos de trabajo renovables por periodos cortos de tiempo (se recomienda trimestralmente), con la finalidad de que sí se detectara algún soborno, se pueda despedir a los involucrados sin mayor contratiempo.</li>
                <li>Revisión y firma periódica de ordenamientos jurídico administrativos que penan el soborno por parte de los empleados y directivos. La norma lo dispone pues considera que las personas aceptan el soborno porque no tienen claras en muchas ocasiones las sanciones legales que ello acarrea.</li>
                <li>Dependencia directa del órgano de gobierno. Un sistema de gestión de esta naturaleza no debe depender de la Dirección General. Se consigna claramente que la información debe fluir al máximo órgano decisorio, de tal forma que no se contamine ninguna investigación. Si se decidiera establecer un SGAS en un ayuntamiento, el Cabildo, como máximo órgano de autoridad, debe hacer las veces de consejo directivo y recibir los reportes de auditoría interna del sistema, por dar un ejemplo preciso.</li>
                <li>Identificación de partes interesadas. Se refiere a la necesidad de identificar a todos los actores internos y externos que requieren desterrar los sobornos de su trabajo diario, por ello, el mapa de riesgos deberá identificar las áreas que pueden tener este problema y realizar un conjunto de acciones preventivas documentadas que lo limiten.</li>
                <li>La norma establece que se deben poner montos específicos para acciones de hospitalidad, entendida como comidas, viajes, transportación, y hospedajes, entre otros, a pagar por parte de la organización a las personas con quienes se realizan negocios, de tal manera que haya evidencia que cualquier pago desproporcionado.</li>
                <li>Se debe establecer un procedimiento claro para que el empleado sepa que hacer exactamente en el momento que reciba una proposición de soborno, para que quede a salvo su empleo.</li>
                <li>Todas las personas de la organización deben saber que el SGAS está funcionando en la empresa, para que se envíe el mensaje claro de que la lucha contra el soborno es una política institucional.</li>
              </ol>
              <p>
                Como podrán observar, es difícil articular algunos de estos requisitos al sector público, pues se trata de una norma cuya finalidad es proteger a la empresa en la relación que tiene con el gobierno, al punto de llegar a plantear que, si algún funcionario público solicita un soborno por algún trámite, y si no lograr ese trámite puede llevar a la quiebra a la empresa, es necesario que se concrete el soborno.
